                ANNOTATIONPROP_ASSERT.setText(Integer.toString(metric.getNumberOfAnnotationAssertion()));
                
                metric = mc.getDataLevelQualityMetrics();
                ISOLATEDINDIVIDUAL.setText (Integer.toString(metric.getNumberofIsolatedInstance()));
                INSTANCE_USAGE.setText(Double.toString(metric.getInstanceUsage()));
                
                metric =  mc.getSchemaLevelQualityMetrics();
                RELATION_RICHNESS.setText(Double.toString(metric.getRelationshipRichness()));
                CLASS_RICHNESS.setText(Double.toString(metric.getClassRichness()));
                OP_USAGE.setText(Double.toString(metric.getObjectPropertyUsage()));
